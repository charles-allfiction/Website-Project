    <h1><b><i>Types of Mental Health Problems</i></b></h1>
        <center><b><h3><i>TRIGGER WARNING: Contents about mental health.</h3></b></i><br>
            <i><p>
            Someone you know right now will have a mental health problem, they just don’t know how to tell you.<br>
            Learning a few things about mental health problems might help you to feel more confident about talking and listening. </center></i></p>
    <article>

        <ul>

        <li><h3><b><i>Anxiety & Panic Attacks</i></b></h3>
            Anxiety is a normal emotion that we all experience. You might think of anxiety as feeling stressed, tense, worried, uneasy or scared. Most of the time these emotions are not a problem.</li>

        <li><h3><b><i>Bipolar Disorder</i></b></h3>
            Bipolar disorder is a mood disorder that can cause extreme periods of low (depressed) and high (manic) moods. Bipolar used to be called manic depression.</li>

        
        <li><h3><b><i>Depression</i></b></h3>
            Depression is when someone experiences a low mood that lasts for a long time and affects their everyday life. </li>

        <li><h3><b><i>Obsessive-Compulsive Disorder (OCD)</i></b></h3>
            OCD is a diagnosis given to someone who experiences obsessive thoughts and compulsive behaviours.</li>

        <li><h3><b><i>Personality Disorders</i></b></h3>
            A personality disorder can affect how a person copes with day-to-day life and manages relationships, as well as how they feel and behave.</li>

        <li><h3><b><i>Post Traumatic Stress Disorder (PTSD)</i></b></h3>
            PTSD is a diagnosis given to people who develop a certain set of symptoms following a traumatic event.</li>
        
        <li><h3><b><i>Psychosis</i></b></h3>
            Psychosis is a term used to describe when a person perceives the world in a different way to those around them. This can include how a person will experience, believe or view things.</li>

        <li><h3><b><i>Schizoaffective Disorder</i></b></h3>
            Schizoaffective disorder is a mental illness that affects a person’s mood, thoughts and behaviour. Around one in 200 people develop schizoaffective disorder and some point in their life.</li>

        <li><h3><b><i>Schizophrenia</i></b></h3>
            Schizophrenia is a mental illness that affects the way a person thinks. Schizophrenia affects around 1 in 100 people and can only be diagnosed by a psychiatrist.</li>

        <li><h3><b><i>Self Harm</i></b></h3>
            Self-harm is when someone purposely hurts themselves, usually in order to cope with intense emotional distress.</li>

        <li><h3><b><i>Suicidal Feelings</i></b></h3>
            Suicide - when someone intentionally takes their own life – can be very complex.</li>
        </ul>

