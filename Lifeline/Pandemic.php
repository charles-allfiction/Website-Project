    <h1><b><i>Coping with Mental Health During Lockdown</i></b></h1>
    <article>
        When you already have an anxiety disorder, and a real pandemic hits, you can feel especially lost and terrified. <br>

        Clinical psychologist Regine Galanti, Ph.D, helps her clients recognize that their anxiety is a false alarm—“it’s not your house on fire, it’s a pizza burning in the toaster.” But because of Coronavirus, she said, your house is actually ablaze. In other words, it makes sense that you’re anxious.<br><br>

        It makes sense that your symptoms have flared up or gotten worse, agreed Emily Bilek, Ph.D, a clinical psychologist and clinical assistant professor at University of Michigan.<br><br>

        Bilek noted that people understandably have genuine fears about their jobs, their health, their homes, their finances, and the pandemic’s short- and long-term impact on society.<br><br>

        But while your anxiety might be peaking, there are many helpful actions you can take. One of the best steps is to schedule a teletherapy session with your therapist (or find a therapist to work with). Here are other tips to try:<br><br>

        <ul><li>Set limits. Keeping the TV on your favorite news network and scrolling social media all day long puts you in a constant state of anxiety. “Hearing about all the danger increases our perception of the threat,” said Galanti, who has a private practice in Long Island, N.Y. Instead, she encouraged readers to carve out specific times to check for updates. This way you stay informed without being blindsided and bombarded with negative information.</li></ul>

        <ul><li>Another helpful limit to set is not talking about the pandemic: “Tell your friends and family that you’ll be changing the subject when it comes up,” Galanti said. “This will not only help limit your anxiety, but also help others as well.”</li></ul>

        <ul><li>Practice sustainable self-care. Prior to the pandemic, you might’ve relied on a slew of self-care practices: You went to a specific yoga studio you love, meditated on your commute, and took long weekend walks. Not having these habits when you need them most might lead you to over-do it at home.</li></ul>
