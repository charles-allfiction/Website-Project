<h1><b><i>How to Support Someone with Mental Health Problems</i></b></h1>

    <article>
        <ul>
        <li><h4><b><i>How do I know if someone has a mental health problem?</i></b></h4></li>
        </ul>

        <ul><p>Sometimes it will seem obvious when someone is going through a hard time, but there is no simple way of knowing if they have a mental health problem. Sometimes you don’t need to know. It’s more important to respond sensitively to someone who seems troubled than to find out whether or not they have a diagnosis.<br><br>

        Although certain symptoms are common with specific mental health problems, no two people behave in exactly the same way when they are unwell. If you know the person well, you may notice changes in their behaviour or mood.<br><br>

        If you are worried about someone it can be difficult to know what to do. When you are aware there is an issue, it is important not to wait. Waiting and hoping they will come to you for help might lose valuable time in getting them support.<br><br>

        Talking to someone is often the first step to take when you know they are going through a hard time. This way you can find out what is troubling them and what you can do to help.<br><br></p></ul>

        <ul>
        <li><h4><b><i>Tips for Talking About Mental Health</h4></b></i>
        </li>

        <ol>
        <li><h3><b><i>Set time aside with no distractions.</i></b></h3>
            It is important to provide an open and non-judgemental space with no distractions.</li>

        <li><h3><b><i>Let them share as much or as little as they want to.</i></b></h3>
            Let them lead the discussion at their own pace. Don’t put pressure on them to tell you anything they aren’t ready to talk about. Talking can take a lot of trust and courage. You might be the first person they have been able to talk to about this.</li>

        
        <li><h3><b><i>Don't try to diagnose or second guess their feelings.</i></b></h3>
            You probably aren’t a medical expert and, while you may be happy to talk and offer support, you aren’t a trained counsellor. Try not to make assumptions about what is wrong or jump in too quickly with your own diagnosis or solutions.</li>

        <li><h3><b><i>Keep questions open ended.</i></b></h3>
            Say "Why don’t you tell me how you are feeling?" rather than "I can see you are feeling very low". Try to keep your language neutral. Give the person time to answer and try not to grill them with too many questions.</li>

        <li><h3><b><i>Talk about wellbeing.</i></b></h3>
            Talk about ways of de-stressing or practicing self-care and ask if they find anything helpful. Exercising, having a healthy diet and getting a good nights sleep can help protect mental health and sustain wellbeing. </li>

        <li><h3><b><i>Listen carefully to what they tell you.</i></b></h3>
            Repeat what they have said back to them to ensure you have understood it. You don’t have to agree with what they are saying, but by showing you understand how they feel, you are letting them know you respect their feelings.</li>

        <li><h3><b><i>Offer them help in seeking professional support and provide information on ways to do this.</i></b></h3>
            You might want to offer to go the GP with them, or help them talk to a friend or family member. Try not to take control and allow them to make decisions.</li>

        <li><h3><b><i>Know your limits.</i></b></h3>
            Ask for help or signpost if the problem is serious. If you believe they are in immediate danger or they have injuries that need medical attention, you need to take action to make sure they are safe. More details on dealing in a crisis can be found below.</li>

        </ol>

