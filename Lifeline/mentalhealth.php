    <h1><b><i>How the Pandemic has Affected People's Mental Health</i></b></h1>
	<article>
		The year 2020 will go down in history as one of the most devastating in history. Hundreds of thousands have died and millions have been hospitalized due to the novel coronavirus pandemic. COVID-19 has changed the lives of so many.<br><br>

        No matter where you live, dealing with the effects of economic and physical lockdowns in a community leads to multiple mental health challenges. After months of living with the coronavirus, many people are getting tired, burned out, and more and more frustrated.<br><br>

        In a new survey published in the journal, Pediatrics (Patrick et al., 2020), we learn from a study of 1,011 parents just how much of a toll the pandemic has taken on the mental health of families. Over a quarter of people agreed that their mental health has gotten worse. And it’s no wonder — nearly half of people said they had lost access to childcare, a cornerstone of so many family’s stability.<br><br>

        Large numbers of people — nearly 40% of those responding to the survey — said they are forgoing children’s doctor’s visits out of fear of the coronavirus. The survey, called the Vanderbilt Child Health COVID-19 Poll, was distributed during the first week of June 2020.<br><br>

        Additional recently-published research suggests that COVID-19 may come with longer-term mental health consequences. Mazza et al. (2020) looked at the psychiatric health of 402 adults who had survived a COVID-19 infection a month after being discharged from the hospital.<br><br>

        The results were not encouraging. From both a clinical interview and a number of self-report measures, the researchers found that many of the recovered patients suffered from significant psychiatric symptoms:<br>

        <ul><li>28% for PTSD, 31% for depression, 42% for anxiety, 20% for [obsessive-compulsive] symptoms, and 40% for insomnia.</li></ul>

        In short, it appears from this early research that if you get seriously ill from COVID-19 and require hospitalization, you’d be in the minority to come away from the hospitalization without having significant psychiatric symptoms a month later. To be fair, some have called some of the study’s findings into question.<br><br>

        We’re only starting to understand what the long-term ramifications of a COVID-19 infection are. And while many are focused on potential chronic health problems associated with the disease, this is one of the first studies to look at the possible long-term mental health problems.<br><br>
